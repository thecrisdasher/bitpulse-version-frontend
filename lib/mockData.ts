export const getTopGainers = (cryptos = cryptocurrencies, count = 5) => {
  return [...cryptos].sort((a, b) => b.change24h - a.change24h).slice(0, count)
}

export const getTopLosers = (cryptos = cryptocurrencies, count = 5) => {
  return [...cryptos].sort((a, b) => a.change24h - b.change24h).slice(0, count)
}

export const getTrending = (cryptos = cryptocurrencies, count = 5) => {
  return [...cryptos].sort((a, b) => b.volume24h - a.volume24h).slice(0, count)
}

